// up to 32-bits
static unsigned jo_readBits(const unsigned char *data, int *at, int num) {
	unsigned r = 0;
	// read partial starting bits
	int sc = (8 - (*at & 7)) & 7;
	sc = sc > num ? num : sc;
	if(sc) {
		r = (data[*at/8] >> (8 - (*at&7) - sc)) & ((1<<sc)-1);
		num -= sc;
		*at += sc;
	}
	// read full bytes
	while(num>=8) {
		r <<= 8;
		r |= data[*at/8];
		*at += 8;
		num -= 8;
	}
	// read partial ending bits
	if(num) {
		r <<= num;
		r |= (data[*at/8] >> (8 - num)) & ((1<<num)-1);
		*at += num;
	}
	return r;
}


